# Objetivo: calcular o montante com juros simples
# Fórmula: M = C * (1 + i * t)

# valores de exemplo
C = 1000     # capital inicial
i = 0.05     # taxa de juros (5% ao mês)
t = 6        # tempo (6 meses)

M = C * (1 + i * t)

print("Capital inicial (C):", C)
print("Taxa de juros (i):", i)
print("Tempo (t):", t)
print("Montante (M):", M)
